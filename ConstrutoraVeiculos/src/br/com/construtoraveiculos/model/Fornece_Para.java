package br.com.construtoraveiculos.model;

public class Fornece_Para {

	private int fpaPeca;
	private int fpaFornecedor;
	private int fpaProejeto;
	private int fpaQtd;

	public int getFpaPeca() {
		return fpaPeca;
	}

	public void setFpaPeca(int fpaPeca) {
		this.fpaPeca = fpaPeca;
	}

	public int getFpaFornecedor() {
		return fpaFornecedor;
	}

	public void setFpaFornecedor(int fpaFornecedor) {
		this.fpaFornecedor = fpaFornecedor;
	}

	public int getFpaProejeto() {
		return fpaProejeto;
	}

	public void setFpaProejeto(int fpaProejeto) {
		this.fpaProejeto = fpaProejeto;
	}

	public int getFpaQtd() {
		return fpaQtd;
	}

	public void setFpaQtd(int fpaQtd) {
		this.fpaQtd = fpaQtd;
	}

}
