package br.com.construtoraveiculos.model;

public class Fornecedor {

	private int forCodigo;
	private String forNome;
	private int forCategoria;

	public int getForCodigo() {
		return forCodigo;
	}

	public void setForCodigo(int forCodigo) {
		this.forCodigo = forCodigo;
	}

	public String getForNome() {
		return forNome;
	}

	public void setForNome(String forNome) {
		this.forNome = forNome;
	}

	public int getForCategoria() {
		return forCategoria;
	}

	public void setForCategoria(int forCategoria) {
		this.forCategoria = forCategoria;
	}

}
